print("Operador IGUAL (==)")
print(5 == 7) # FALSE
print(10 == 7) # FALSE
print(5.0 == 5) # TRUE

print("\nOperador DIFERENTE O NO IGUAL (!=)")
print(5 != 5) # FALSE
print(10 != 7) # TRUE
print(5.0 != 5) # FALSE

print("\nOperador MAYOR QUE (>)")
print(5 > 5) # FALSE
print(10 > 7) # TRUE 
print(5.0 > 5) # FALSE 

print("\nOperador MENOR QUE (<)")
print(5 < 10) # TRUE
print(10 < 5) # FALSE 

print("\nOperador MENOR O IGUAL QUE (<=)")
print(5 <= 5) # TRUE
print(10 <= 7) # FALSE
print(3 <= 5) # TRUE

print("\nOperador MAYOR O IGUAL QUE (>=)")
print(5 >= 5) # TRUE
print(10 >= 7) # TRUE
print(3 >= 5) # FALSE

