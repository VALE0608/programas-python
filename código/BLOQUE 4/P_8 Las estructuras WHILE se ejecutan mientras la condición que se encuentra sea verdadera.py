# Ejemplo 1: imprimir los números del 1 al 10
print("Números del 1 al 10")
i = 1 # Inicialización de la variable control
while i <= 10:# Condición de parada
    print(i)
    i += 1 # Equivalente a i = i + 1

# Sintaxis 
# while <condición>:
#   <cuerpo del while>

# Ejemplo 2
# Imprimir los números del 10 al 1
print("\nNúmeros del 10 al 1")
i = 10
while i >= 1:
    print(i)
    i -= 1
